    public void pesquisarVenda(Integer id) {
        try {
            Venda venda = dominioController.buscarVenda(id);
            txtDtVenda.setText(venda.getDataVenda());
            txtQtdeVendida.setText(venda.getQtdeVenda().toString());
            txtTotal.setText(venda.getValorTotal().toString());
            Produto produto = dominioController.buscarProdutoPorId(venda.getProduto().getIdProduto());
            cmbProdutos.getModel().setSelectedItem(produto);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar venda");
        }
    }
    

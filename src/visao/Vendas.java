    public void carregarDropProdutos(JComboBox drop){
        try {
            List<Produto> listaProdutos = dominioController.obterProdutos();
            drop.setModel(new DefaultComboBoxModel(listaProdutos.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao listar Produtos");
        }
    }
    
    public void limparCamposProduto(){
        txtNome.setText("");
        txtQtde.setText("");
        txtPreco.setText("");
    }
    
    public void limparCamposVenda(){
        txtDtVenda.setText("");
        txtQtdeVendida.setText("");
        txtTotal.setText("");
    }
    
    public void inserirVenda(){
        try {
            dominioController.inserirVenda(txtDtVenda.getText(), Integer.parseInt(txtQtdeVendida.getText()), Double.parseDouble(txtTotal.getText()), (Produto)cmbProdutos.getSelectedItem());
            JOptionPane.showMessageDialog(this, "Venda de " + cmbProdutos.getSelectedItem().toString() + " cadastrada com sucesso");
            limparCamposVenda();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao inserir Venda");
        }
    }
    
    public void inserirProduto(){
        try {
            dominioController.inserirProduto(txtNome.getText(), Integer.parseInt(txtQtde.getText()), Double.parseDouble(txtPreco.getText()));
            limparCamposProduto();
            JOptionPane.showMessageDialog(this, "Produto " + txtNome.getText() + " inserido com sucesso");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao inserir Produto");
        }
    }

